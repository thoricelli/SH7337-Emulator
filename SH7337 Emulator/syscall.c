void execute_syscall(sh7337_t *cpu_state) {

	//Syscall number is stored in R0.
	unsigned long syscall_no = cpu_state->bank0->R[0];
	fx_syscall_t syscall = syscalls[syscall_no];

	if (syscall.function == NULL)
		printf("Syscall %s not implemented, ignoring.\n", syscall.name);
	else {
		printf("Executing syscall: %s (%lX)\n", syscall.name, syscall_no);
	}

	//Return to jump address.
	set_pc_little_endian(cpu_state->PR);
}